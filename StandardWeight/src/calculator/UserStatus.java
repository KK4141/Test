package calculator;

class UserStatus {

	private int sex;
	private int age;
	private double height;
	private double weight;

	int getSex() {
		return sex;
	}
	void setSex(int sex) {
		this.sex = sex;
	}
	int getAge() {
		return age;
	}
	void setAge(int age) {
		this.age = age;
	}
	double getWeight() {
		return weight;
	}
	void setWeight(double weight) {
		this.weight = weight;
	}
	double getHeight() {
		return height;
	}
	void setHeight(double height) {
		this.height = height;
	}

}
